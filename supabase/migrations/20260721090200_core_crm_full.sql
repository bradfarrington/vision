-- ============================================================================
-- Apply the identical tenant-isolation treatment to every company_id table.
-- Looping guarantees no table is left with RLS off or a mismatched policy.
-- ============================================================================
do $$
declare
  t text;
  tenant_tables text[] := array[
    'suppliers','staff_members','quote_types','contract_types','lead_results',
    'products','stock_items','stock_locations','inventory','contracts','quotes',
    'jobs','appointments','documents','lead_notes','contract_notes',
    'contract_products','finance_lines','finance_payments','delivery_lines',
    'commissions','job_invoices','fitting_appointments','activities','messages',
    'company_settings','checklist_templates','lead_checklist_items',
    'contract_checklist_templates','contract_checklist_items','notifications',
    'chat_channels','chat_messages','stock_transactions','email_domains',
    'email_sender_addresses','user_email_settings','email_templates','email_campaigns'
  ];
begin
  foreach t in array tenant_tables loop
    execute format('create index if not exists %I on public.%I (company_id)',
                   t || '_company_id_idx', t);
    execute format('alter table public.%I enable row level security', t);
    execute format(
      'create policy "tenant isolation" on public.%I for all to authenticated '
      'using (company_id = public.current_company_id()) '
      'with check (company_id = public.current_company_id())', t);
  end loop;
end $$;

-- ---- Junction tables: no company_id; isolate via the parent ----------------
alter table public.chat_channel_members enable row level security;
create policy "tenant isolation via channel"
  on public.chat_channel_members for all to authenticated
  using ( exists (
    select 1 from public.chat_channels c
    where c.id = channel_id and c.company_id = public.current_company_id() ) )
  with check ( exists (
    select 1 from public.chat_channels c
    where c.id = channel_id and c.company_id = public.current_company_id() ) );

alter table public.chat_message_reads enable row level security;
create policy "tenant isolation via message"
  on public.chat_message_reads for all to authenticated
  using ( exists (
    select 1 from public.chat_messages m
    where m.id = message_id and m.company_id = public.current_company_id() ) )
  with check ( exists (
    select 1 from public.chat_messages m
    where m.id = message_id and m.company_id = public.current_company_id() ) );
