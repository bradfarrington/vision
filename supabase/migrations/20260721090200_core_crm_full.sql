-- ============================================================================
-- Vision CRM — Wave 2: remaining tenant tables
-- ============================================================================
-- Ports the rest of schema.ts into Supabase, applying the SAME tenant-isolation
-- shape proven in Wave 1: company_id NOT NULL + FK -> companies + index, RLS
-- enabled, one FOR ALL policy scoped to current_company_id(). The RLS/index/
-- policy is applied uniformly by the loop at the bottom, so every table is
-- guaranteed the identical treatment — no table can be accidentally left open.
--
-- Two junction tables (chat_channel_members, chat_message_reads) have no
-- company_id of their own and are isolated through their parent instead.
-- ============================================================================

